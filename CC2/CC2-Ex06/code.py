
score = input("Enter score: ")
fscore = float(score)
if fscore > 1.0 :
    print("Bad Score")
elif fscore < 0.0 :
    print("Bad Score")
elif fscore >= .9 :
    print('A')
elif fscore >= .8 :
    print('B')
elif fscore >= .7 :
    print("C")
elif fscore >= .6 :
    print("D")
elif fscore < .6 :
    print('F')
   